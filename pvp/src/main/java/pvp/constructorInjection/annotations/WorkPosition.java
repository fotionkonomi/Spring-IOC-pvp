package pvp.constructorInjection.annotations;

public class WorkPosition {

	private int id;
	private String name;

	public WorkPosition(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
